CREATE TABLE client (
    id INT PRIMARY KEY,
    firstname VARCHAR(255),
    lastname VARCHAR(255),
    email VARCHAR(255),
    phone VARCHAR(255),
    adress VARCHAR(255),
    city VARCHAR(255),
    country VARCHAR(255),
    username VARCHAR(255),
    password VARCHAR(255),
    avatar BLOB,
    RIB VARCHAR(255),
    BIC VARCHAR(255)
);